function Portfolio() {
  const [filter, setFilter] = useState("all");
  const [projects, setProjects] = useState([]);

  useEffect(() => {
    setProjects(portfolio);
  }, []);

  useEffect(() => {
    setProjects([]);

    const filtered = portfolio.map((p) => ({
      ...p,
      filtered: p.category.includes(filter),
    }));
    setProjects(filtered);
  }, [filter]);

  return (
    <>
      <Navbar />
      <div className="portfolio-container">
        <div className="portfolio__labels">
          <span
            className={filter == "all" ? "active" : ""}
            onClick={() => setFilter("all")}
          >
            All
          </span>
          <span
            className={filter == "movie" ? "active" : ""}
            onClick={() => setFilter("movie")}
          >
            Movie
          </span>
          <span
            className={filter == "music" ? "active" : ""}
            onClick={() => setFilter("music")}
          >
            Music
          </span>
          <span
            className={filter == "feature" ? "active" : ""}
            onClick={() => setFilter("feature")}
          >
            Feature
          </span>
          <span
            className={filter == "documentary" ? "active" : ""}
            onClick={() => setFilter("documentary")}
          >
            Documentary
          </span>
        </div>
        <div className="portfolio__container">
          <div className="portfolio_card">
            {projects.length === 0 ? (
              <img src={Loading} alt="" />
            ) : (
              projects.map((item) =>
                item.filtered === true ? (
                  <>
                    <a href={item.url} key={item.name} target="_blank">
                      <LazyLoadImage
                        className="portfolio_card__wrapper"
                        src={item.imgurl}
                        PlaceholderSrc={item.placeholder}
                        effect="blur"
                      />
                    </a>
                  </>
                ) : (
                  ""
                )
              )
            )}
          </div>
        </div>
      </div>
      <AdminFooter />
    </>
  );
}

export default Portfolio;
